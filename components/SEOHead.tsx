interface SEOHeadProps {
  schemas: any[];
}

export default function SEOHead({ schemas }: SEOHeadProps) {
  return (
    <>
      {schemas.map((schema, index) => (
        <script
          key={index}
          type="application/ld+json"
          dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
        />
      ))}
    </>
  );
}
